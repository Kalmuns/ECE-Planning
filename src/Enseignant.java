
public class Enseignant {
	
	private int id;
	private int courid;
	public Enseignant(int i, int no)
	{
		id=i;
		courid=no;
	}
	public void setID(int i)
	{
		id=i;
	}
	public void setcourid(int no)
	{
		courid=no;
	}
	public int getID()
	{
		return id;
	}
	public int getgetcourid()
	{
		return courid;
	}
}
