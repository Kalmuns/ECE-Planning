
public class Cour {
	private int id;
	private String nom;
	public Cour(int i, String no)
	{
		id=i;
		nom=no;
	}
	public void setID(int i)
	{
		id=i;
	}
	public void setnom(String no)
	{
		nom=no;
	}
	public int getID()
	{
		return id;
	}
	public String getnom()
	{
		return nom;
	}
}
